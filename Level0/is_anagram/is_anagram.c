int is_anagram(char *a, char *b)
{
    int i;
    int chr[95] = {0};

    i = 0;
    while(a[i])
    {
        chr[a[i] - 32] = chr[a[i] - 32] + 1;
        i++;
    }
    i = 0;
    while(b[i])
    {
        chr[b[i] - 32] = chr[b[i] - 32] - 1;
        i++;
    }
    i = 0;
    while (i < 95)
    {
        if (chr[i] != 0)
            return 0;
        i++;
    }
    return 1;
}